DROP TABLE IF EXISTS API_TRANSACTIONS;

CREATE TABLE API_TRANSACTIONS (
  FROM_ACCOUNT BIGINT,
  TO_ACCOUNT BIGINT NOT NULL,
  AMOUNT DECIMAL NOT NULL,
  SENT_AT DATE NULL
);

