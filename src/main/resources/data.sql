CREATE TABLE API_ACCOUNT (
  ACCOUNT BIGINT  PRIMARY KEY,
  `OWNER` BIGINT NOT NULL,
  BALANCE DECIMAL NOT NULL,
  CREATED_AT DATE NULL
);


